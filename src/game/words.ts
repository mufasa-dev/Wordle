export function getRandomWord(lang: string = "pt"): string {
  let words: string[] = [];
  if (lang == "en") {
    words = wordsEn;
  } else {
    words = wordsPt;
  }
  const index = Math.floor(Math.random() * words.length);
  return words[index];
}
